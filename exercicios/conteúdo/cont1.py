num1=input('digite algo')
print(num1.isnumeric()) #para testar numeros 
print(num1.isalpha()) #para testa letras
print(num1.isalnum()) #testa se é letra ou numero
print(num1.isupper()) #verificar se tem letra maiuscula
print(num1.isdecimal()) #verificar se tem numero decimal
print(num1.islower()) #verificar se tem letras minusculas
print(num1.isspace()) #verificar se tem espaço