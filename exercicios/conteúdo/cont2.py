# + é usado para a soma 
# - é usado para a subtração
# * é usado para a multiblicação
# / é usado para a divisão
# ** é usado para a exponenciação
# // é usado para a divisão inteira 
# % é igual ao resto da divisão
# == é igual ao sinal de igual

print(5+2)
print(5-2)
print(5*2)
print(5/2)
print(5**2)
print(5//2)
print(5%2)
print(5==2)
print(5==(2.5*2))

